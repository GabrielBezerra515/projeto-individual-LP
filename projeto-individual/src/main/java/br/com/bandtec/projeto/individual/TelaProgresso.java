    private void btnValoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValoresActionPerformed
        // TODO add your handling code here:
        Random gerador = new Random();

        Integer aleatorioCpu = gerador.nextInt(101);
        Integer aleatorioDisco = gerador.nextInt(101);
        Integer aleatorioMemoria = gerador.nextInt(101);

        new Thread() {
            @Override
            public void run() {
                for (int i = 0; i < aleatorioCpu; i++) {
                    try {
                        sleep(10);
                        pgCpu.setValue(i);
                    } catch (InterruptedException e) {
                    }
                }
            }
        }.start();
        
        new Thread(){
            @Override
            public void run() {
                for (int i = 0; i < aleatorioDisco; i++) {
                    try {
                        sleep(10);
                        pgDisco.setValue(i);
                    } catch (InterruptedException e) {
                    }
                }
            }
        }.start();
        
        new Thread(){
            @Override
            public void run(){
                for (int i = 0; i < aleatorioMemoria; i++) {
                    try {
                        sleep(10);
                        pgMemoria.setValue(i);
                    } catch (InterruptedException e) {
                    }   
                }
            }
        }.start();
    }//GEN-LAST:event_btnValoresActionPerformed

